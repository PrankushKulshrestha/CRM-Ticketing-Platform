const DEFAULT_TREND_DAYS = 30 as const;
const MAX_TREND_DAYS = 366 as const;

const OPEN_STATUSES = ["new", "open", "pending", "reopened", "request_clarification", "in_progress"] as const;
const CLOSED_STATUSES = ["resolved", "closed"] as const;

/* -------------------------------------------------------------------------- */
/* Helpers                                                                    */
/* -------------------------------------------------------------------------- */

const safeNumber = (value: unknown): number => {
  return typeof value === "number" && Number.isFinite(value) ? value : 0;
};

/*
 * FIX: previously used `new Date()` + `setHours(0,0,0,0)`, which clamps to
 * LOCAL midnight (server timezone) — but MongoDB's $dateToString buckets
 * days in UTC by default. On a server running ahead of UTC (e.g. IST,
 * UTC+5:30), local midnight is still the previous UTC day, so
 * `.toISOString().slice(0,10)` on it yields YESTERDAY's date string. Every
 * day-key generated by lastNDays() was then one day behind the Mongo
 * aggregation's actual UTC-bucketed createdDateFormatted/resolvedDateFormatted/
 * closedDateFormatted, so "today's" ticket activity matched no day in the
 * `days[]` window and silently dropped out of created/resolved/closed —
 * only `openCount` survived, since that branch is live-queried independent
 * of date-string matching. Computing midnight directly in UTC fixes the
 * mismatch at the source.
 */
const startOfToday = (): Date => {
  const d = new Date();
  return new Date(Date.UTC(d.getUTCFullYear(), d.getUTCMonth(), d.getUTCDate()));
};

const formatDate = (d: Date): string => d.toISOString().slice(0, 10);

/** Last N calendar day strings (UTC), oldest first, ending today (inclusive). */
function lastNDays(n: number): string[] {
  const days: string[] = [];
  const cursor = startOfToday();
  for (let i = n - 1; i >= 0; i--) {
    const d = new Date(cursor);
    d.setUTCDate(d.getUTCDate() - i);
    days.push(formatDate(d));
  }
  return days;
}

