export interface AgentPerformanceSummary {
  agentId: string;
  name: string;
  email: string;
  role: string;
  totalTickets: number;
  resolvedTickets: number;
  pendingTickets: number;
  openTickets: number;
  avgResolutionHours: number | null;
  avgResponseMinutes: number | null;
  slaBreaches: number;
  csatAvg: number | null;
  fcrRate: number | null;
  reopenedCount: number;
}

export async function getAgentPerformanceList(
  options?: { period?: string },
): Promise<AgentPerformanceSummary[]> {
  const { User } = await import("../models/User");
  const { Ticket } = await import("../models/Ticket");

  const agents = await User.find({
    role: { $in: ["agent", "team_lead", "manager", "admin"] },
    isActive: { $ne: false },
  }).select({ _id: 1, name: 1, email: 1, role: 1 }).lean();

  const results: AgentPerformanceSummary[] = await Promise.all(
    agents.map(async (agent) => {
      const agentId = agent._id as mongoose.Types.ObjectId;

      const [ticketAgg, slaBreaches] = await Promise.all([
        Ticket.aggregate([
          { $match: { tkt_assigned_to: agentId, merged_into: null } },
          {
            $group: {
              _id: null,
              total: { $sum: 1 },
              resolved: { $sum: { $cond: [{ $in: ["$tkt_status", ["resolved", "closed"]] }, 1, 0] } },
              pending: { $sum: { $cond: [{ $eq: ["$tkt_status", "pending"] }, 1, 0] } },
              open: { $sum: { $cond: [{ $in: ["$tkt_status", ["open", "new", "reopened", "request_clarification"]] }, 1, 0] } },
              reopened: { $sum: { $cond: [{ $eq: ["$was_reopened", true] }, 1, 0] } },
              avgResolutionMs: {
                $avg: {
                  $cond: [
                    { $and: [{ $ne: ["$resolved_date", null] }, { $ne: ["$created_date", null] }] },
                    { $subtract: ["$resolved_date", "$created_date"] },
                    null,
                  ],
                },
              },
              avgResponseMs: {
                $avg: {
                  $cond: [
                    { $ne: ["$first_response_at", null] },
                    { $subtract: ["$first_response_at", "$created_date"] },
                    null,
                  ],
                },
              },
              csatSum: { $sum: { $ifNull: ["$customer_satisfaction", 0] } },
              csatCount: { $sum: { $cond: [{ $ne: ["$customer_satisfaction", null] }, 1, 0] } },
            },
          },
        ]),
        Ticket.aggregate([
          { $match: { tkt_assigned_to: agentId, merged_into: null } },
          { $lookup: { from: "slas", localField: "_id", foreignField: "ticketId", as: "sla" } },
          { $match: { "sla.status": { $in: ["breached", "sla_violated"] } } },
          { $count: "total" },
        ]).then((r) => r[0]?.total ?? 0),
      ]);

      const agg = ticketAgg[0] ?? {};
      const total = agg.total ?? 0;
      const resolved = agg.resolved ?? 0;
      const fcr = total > 0 && agg.reopened != null
        ? Math.round(((resolved - (agg.reopened ?? 0)) / Math.max(resolved, 1)) * 100)
        : null;

      return {
        agentId: agentId.toString(),
        name: agent.name,
        email: agent.email,
        role: agent.role,
        totalTickets: total,
        resolvedTickets: resolved,
        pendingTickets: agg.pending ?? 0,
        openTickets: agg.open ?? 0,
        reopenedCount: agg.reopened ?? 0,
        avgResolutionHours: agg.avgResolutionMs ? Math.round(agg.avgResolutionMs / 3600000 * 10) / 10 : null,
        avgResponseMinutes: agg.avgResponseMs ? Math.round(agg.avgResponseMs / 60000 * 10) / 10 : null,
        slaBreaches: slaBreaches,
        csatAvg: agg.csatCount > 0 ? Math.round((agg.csatSum / agg.csatCount) * 10) / 10 : null,
        fcrRate: fcr,
      };
    }),
  );

  return results.sort((a, b) => b.resolvedTickets - a.resolvedTickets);
}

export async function getAgentPerformanceById(
  agentId: string,
): Promise<AgentPerformanceSummary & { recentTickets: unknown[] }> {
  const { User } = await import("../models/User");
  const { Ticket } = await import("../models/Ticket");

  const agent = await User.findById(agentId).select({ _id: 1, name: 1, email: 1, role: 1 }).lean();
  if (!agent) throw new ApiError(404, "Agent not found");

  const oid = new mongoose.Types.ObjectId(agentId);

  const summaries = await getAgentPerformanceList();
  const found = summaries.find((s) => s.agentId === agentId) ?? {
    agentId, name: agent.name, email: agent.email, role: agent.role,
    totalTickets: 0, resolvedTickets: 0, pendingTickets: 0, openTickets: 0,
    reopenedCount: 0, avgResolutionHours: null, avgResponseMinutes: null,
    slaBreaches: 0, csatAvg: null, fcrRate: null,
  };

  const recentTickets = await Ticket.find({ tkt_assigned_to: oid, merged_into: null })
    .select({ tkt_number: 1, email_subject: 1, tkt_status: 1, color_code: 1, created_date: 1, resolved_date: 1 })
    .sort({ update_date: -1 })
    .limit(10)
    .lean();

  return { ...found, recentTickets };
}