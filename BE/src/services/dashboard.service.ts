export interface DashboardDataOptions {
  /**
   * Number of trailing calendar days to include in `charts.trends`, clamped
   * to [1, MAX_TREND_DAYS]. Ignored if `full` is true. Defaults to 30 to
   * preserve existing behavior for any caller that doesn't pass options.
   */
  days?: number;

  /**
   * When true, the trend window spans from the date of the earliest ticket
   * in the system through today, instead of a fixed trailing window.
   */
  full?: boolean;
}

async function resolveTrendDays(
  options: DashboardDataOptions,
): Promise<string[]> {
  if (options.full) {
    const earliest = await Ticket.findOne({})
      .sort({ created_date: 1 })
      .select({ created_date: 1 })
      .lean<{ created_date: Date } | null>();

    if (!earliest?.created_date) {
      // No tickets at all yet — fall back to a single-day window (today)
      // so downstream code always has at least one point to render.
      return lastNDays(1);
    }

    const msPerDay = 1000 * 60 * 60 * 24;
    const earliestDate = new Date(earliest.created_date);
    const earliestUtcMidnight = Date.UTC(
      earliestDate.getUTCFullYear(),
      earliestDate.getUTCMonth(),
      earliestDate.getUTCDate(),
    );
    const spanDays =
      Math.floor((startOfToday().getTime() - earliestUtcMidnight) / msPerDay) + 1;

    return lastNDays(Math.min(Math.max(spanDays, 1), MAX_TREND_DAYS));
  }

  const requested = options.days ?? DEFAULT_TREND_DAYS;
  const clamped = Math.min(Math.max(Math.floor(requested) || 1, 1), MAX_TREND_DAYS);
  return lastNDays(clamped);
}

export async function getDashboardData(
  options: DashboardDataOptions = {},
): Promise<DashboardData> {
  const startOfDay = startOfToday();
  const days = await resolveTrendDays(options);
  const windowStart = new Date(`${days[0]}T00:00:00.000Z`);

  logger.info("[DASHBOARD_SERVICE] aggregation_started");

  // Bring any overdue SLA trackers up to date before reading breach counts —
  // see sla.service.ts's syncDueSLAEscalations() for why this replaces the
  // old SLA_HOURS=24 hardcoded re-derivation below.
  await syncDueSLAEscalations();

  const [result] = await Ticket.aggregate<AggregationResult>([
    /*
     * FIX: previously computed SLA breach inline with a flat SLA_HOURS=24
     * constant applied to every ticket regardless of priority — completely
     * disconnected from the real SLA collection (which tracks per-priority,
     * multi-level escalating limits via sla.service.ts). That made dashboard
     * and analytics each invent their own answer and disagree. Now we join
     * against the actual SLA tracker per ticket instead.
     */
    {
      $lookup: {
        from: "slas",
        localField: "_id",
        foreignField: "ticketId",
        as: "slaDoc",
      },
    },
    {
      $addFields: {
        slaStatus: { $arrayElemAt: ["$slaDoc.status", 0] },
        slaLevel: { $arrayElemAt: ["$slaDoc.currentLevel", 0] },
      },
    },
    {
      $addFields: {
        resolutionTimeHours: {
          $cond: [
            { $in: ["$tkt_status", CLOSED_STATUSES] },
            {
              $divide: [
                { $subtract: ["$update_date", "$created_date"] },
                1000 * 60 * 60,
              ],
            },
            null,
          ],
        },

        slaBreachedComputed: {
          $in: ["$slaStatus", [SLA_STATUSES.BREACHED, SLA_STATUSES.SLA_VIOLATED]],
        },

        createdDateFormatted: {
          $dateToString: { format: "%Y-%m-%d", date: "$created_date" },
        },
        resolvedDateFormatted: {
          $cond: [
            { $ne: ["$resolved_date", null] },
            { $dateToString: { format: "%Y-%m-%d", date: "$resolved_date" } },
            null,
          ],
        },
        closedDateFormatted: {
          $cond: [
            { $ne: ["$closed_date", null] },
            { $dateToString: { format: "%Y-%m-%d", date: "$closed_date" } },
            null,
          ],
        },
      },
    },

    {
      $facet: {
        metrics: [
          {
            $group: {
              _id: null,
              totalTickets: { $sum: 1 },
              openTickets: {
                $sum: { $cond: [{ $in: ["$tkt_status", OPEN_STATUSES] }, 1, 0] },
              },
              /*
               * BUG FIX: previously used closedTicketsCount as denominator, which
               * returned 100% whenever breached tickets were still open (not yet
               * closed), because closedTicketsCount=0 triggered the 100% guard.
               * Correct denominator is tickets that have an SLA tracker attached
               * (slaTrackedCount). A breach is a breach regardless of ticket status.
               */
              slaTrackedCount: {
                $sum: { $cond: [{ $ifNull: ["$slaStatus", false] }, 1, 0] },
              },
              resolvedToday: {
                $sum: {
                  $cond: [
                    {
                      $and: [
                        { $in: ["$tkt_status", CLOSED_STATUSES] },
                        { $gte: ["$update_date", startOfDay] },
                      ],
                    },
                    1,
                    0,
                  ],
                },
              },
              slaBreaches: { $sum: { $cond: ["$slaBreachedComputed", 1, 0] } },
              avgResolutionTime: { $avg: "$resolutionTimeHours" },
            },
          },
        ],

        /*
         * Per-level SLA health: how many ACTIVE tickets currently sit at
         * each of the 5 escalation levels, plus how many of those are
         * already breached at that level. Powers the "all 5 levels
         * individually visible" SLA health view on the frontend.
         */
        slaByLevel: [
          { $match: { slaLevel: { $ne: null } } },
          {
            $group: {
              _id: "$slaLevel",
              count: { $sum: 1 },
              breached: {
                $sum: {
                  $cond: [
                    { $in: ["$slaStatus", [SLA_STATUSES.BREACHED, SLA_STATUSES.SLA_VIOLATED]] },
                    1,
                    0,
                  ],
                },
              },
            },
          },
          { $sort: { _id: 1 } },
        ],

        createdTrend: [
          { $match: { created_date: { $gte: windowStart } } },
          { $group: { _id: "$createdDateFormatted", count: { $sum: 1 } } },
          { $sort: { _id: 1 } },
        ],

        resolvedTrend: [
          {
            $match: {
              resolvedDateFormatted: { $ne: null },
              resolved_date: { $gte: windowStart },
            },
          },
          { $group: { _id: "$resolvedDateFormatted", count: { $sum: 1 } } },
          { $sort: { _id: 1 } },
        ],

        closedTrend: [
          {
            $match: {
              closedDateFormatted: { $ne: null },
              closed_date: { $gte: windowStart },
            },
          },
          { $group: { _id: "$closedDateFormatted", count: { $sum: 1 } } },
          { $sort: { _id: 1 } },
        ],

        categoryDistribution: [
          {
            $group: {
              _id: { $ifNull: ["$cat_id", "Uncategorized"] },
              value: { $sum: 1 },
            },
          },
          { $sort: { value: -1 } },
        ],

        subCategoryDistribution: [
          {
            $group: {
              _id: { $ifNull: ["$sub_cat_id", "Unknown"] },
              value: { $sum: 1 },
            },
          },
          { $sort: { value: -1 } },
        ],

        agentPerformance: [
          {
            $group: {
              _id: { $ifNull: ["$tkt_user", "Unassigned"] },
              totalTickets: { $sum: 1 },
              resolvedTickets: {
                $sum: { $cond: [{ $in: ["$tkt_status", CLOSED_STATUSES] }, 1, 0] },
              },
              pendingTickets: {
                $sum: { $cond: [{ $eq: ["$tkt_status", "pending"] }, 1, 0] },
              },
            },
          },
          { $sort: { resolvedTickets: -1 } },
        ],

        statusDistribution: [
          {
            $group: {
              _id: { $ifNull: ["$tkt_status", "unknown"] },
              value: { $sum: 1 },
            },
          },
          { $sort: { value: -1 } },
        ],

        /*
         * FIX: customerSatisfaction previously didn't exist on this
         * aggregation at all — DashboardMetrics never carried the field,
         * so the Analytics page's CSAT card always fell back to its
         * client-side default of 0. customer_satisfaction is a 1-5 rating
         * an agent records on the ticket (see Ticket model); this averages
         * whatever's been recorded and expresses it as a 0-100 score to
         * match the card's percentage display (rating/5 * 100).
         */
        customerSatisfaction: [
          { $match: { customer_satisfaction: { $ne: null } } },
          {
            $group: {
              _id: null,
              avgRating: { $avg: "$customer_satisfaction" },
              count: { $sum: 1 },
            },
          },
        ],

        recentActivity: [
          { $sort: { update_date: -1 } },
          { $limit: 10 },
          {
            $project: {
              _id: 0,
              ticketNumber: { $toString: "$_id" },
              title: { $ifNull: ["$email_subject", "Untitled Ticket"] },
              customer: { $ifNull: ["$tkt_customer_name", "Unknown Customer"] },
              assignedTo: { $ifNull: ["$tkt_user", "Unassigned"] },
              category: { $ifNull: ["$cat_id", "Uncategorized"] },
              subCategory: { $ifNull: ["$sub_cat_id", "Unknown"] },
              status: { $ifNull: ["$tkt_status", "unknown"] },
              updatedAt: "$update_date",
              createdAt: "$created_date",
            },
          },
        ],
      },
    },
  ]);

  const data = result ?? {};
  const metricsRaw = data.metrics?.[0] ?? {};

  const slaBreaches = safeNumber(metricsRaw.slaBreaches);
  const slaTrackedCount = safeNumber(metricsRaw.slaTrackedCount);

  // BUG FIX: previously divided by closedTicketsCount, which returned 100%
  // whenever breached tickets were still open (closedTicketsCount=0 guard
  // short-circuited to 100). Now divides by slaTrackedCount — every ticket
  // that has an SLA tracker, regardless of whether it is still open.
  const slaCompliance =
    slaTrackedCount === 0
      ? 100
      : Number(
          (((slaTrackedCount - slaBreaches) / slaTrackedCount) * 100).toFixed(1),
        );

  const csatPoint = data.customerSatisfaction?.[0];
  const customerSatisfaction =
    csatPoint && csatPoint.count > 0
      ? Number(((csatPoint.avgRating / 5) * 100).toFixed(1))
      : null;

  const metrics: DashboardMetrics = {
    totalTickets: safeNumber(metricsRaw.totalTickets),
    openTickets: safeNumber(metricsRaw.openTickets),
    resolvedToday: safeNumber(metricsRaw.resolvedToday),
    slaBreaches,
    avgResolutionTime: safeNumber(metricsRaw.avgResolutionTime),
    slaCompliance,
    customerSatisfaction,
  };

  /* ── Merge the three event trends + the computed open-trend into one series, one point per day ── */

  const createdMap = new Map(
    (data.createdTrend ?? []).map((p) => [p._id, p.count]),
  );
  const resolvedMap = new Map(
    (data.resolvedTrend ?? []).map((p) => [p._id, p.count]),
  );
  const closedMap = new Map(
    (data.closedTrend ?? []).map((p) => [p._id, p.count]),
  );
  const openMap = await computeOpenTrend(days);

  const trends: TrendPoint[] = days.map((day) => ({
    _id: day,
    count: createdMap.get(day) ?? 0,
    resolvedCount: resolvedMap.get(day) ?? 0,
    closedCount: closedMap.get(day) ?? 0,
    openCount: openMap.get(day) ?? 0,
  }));

  // All 5 levels always present (zero-filled) so the frontend can render a
  // consistent 5-bar/5-segment view even when some levels have no tickets.
  const slaByLevelMap = new Map(
    (data.slaByLevel ?? []).map((p) => [p._id, p]),
  );
  const slaByLevel: SLALevelHealthPoint[] = [1, 2, 3, 4, 5].map((level) => {
    const point = slaByLevelMap.get(level);
    const count = point?.count ?? 0;
    const breached = point?.breached ?? 0;
    return { level, count, breached, healthy: count - breached };
  });

  return {
    metrics,
    charts: {
      trends,
      categoryDistribution: data.categoryDistribution ?? [],
      subCategoryDistribution: data.subCategoryDistribution ?? [],
      statusDistribution: data.statusDistribution ?? [],
      agentPerformance: data.agentPerformance ?? [],
      slaByLevel,
    },
    activity: data.recentActivity ?? [],
  };
}
