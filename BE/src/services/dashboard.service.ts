/* -------------------------------------------------------------------------- */
/* Open-trend (cumulative running balance)                                   */
/* -------------------------------------------------------------------------- */

/**
 * "Open as of EOD day X" = tickets created on/before X
 *                          minus tickets resolved on/before X
 *                          minus tickets closed on/before X.
 *
 * This is NOT an event count like createdTrend/resolvedTrend/closedTrend —
 * it's a running balance, so it can't come from a simple $group by day.
 * We instead fetch per-day created/resolved/closed counts across the full
 * ticket history (not just the last 30 days, since older tickets still
 * contribute to the running total) and accumulate.
 *
 * Today's value is always computed fresh from current ticket state on
 * every request (no caching), so reloading mid-day after resolving a
 * ticket reflects the change immediately, per spec.
 */
async function computeOpenTrend(
  days: string[],
): Promise<Map<string, number>> {
  const earliestDay = days[0];
  const earliestDate = new Date(`${earliestDay}T00:00:00.000Z`);

  // Cumulative counts as of the day BEFORE our window starts, so the
  // running balance inside the window is correct even though older
  // history isn't displayed.
  const [createdBefore, resolvedBefore, closedBefore] = await Promise.all([
    Ticket.countDocuments({ created_date: { $lt: earliestDate } }),
    Ticket.countDocuments({ resolved_date: { $lt: earliestDate, $ne: null } }),
    Ticket.countDocuments({ closed_date: { $lt: earliestDate, $ne: null } }),
  ]);

  // Per-day deltas within the window, for each of the three event types.
  const [createdDeltas, resolvedDeltas, closedDeltas] = await Promise.all([
    Ticket.aggregate<DateOnlyPoint>([
      { $match: { created_date: { $gte: earliestDate } } },
      {
        $group: {
          _id: { $dateToString: { format: "%Y-%m-%d", date: "$created_date" } },
          count: { $sum: 1 },
        },
      },
    ]),
    Ticket.aggregate<DateOnlyPoint>([
      { $match: { resolved_date: { $gte: earliestDate, $ne: null } } },
      {
        $group: {
          _id: { $dateToString: { format: "%Y-%m-%d", date: "$resolved_date" } },
          count: { $sum: 1 },
        },
      },
    ]),
    Ticket.aggregate<DateOnlyPoint>([
      { $match: { closed_date: { $gte: earliestDate, $ne: null } } },
      {
        $group: {
          _id: { $dateToString: { format: "%Y-%m-%d", date: "$closed_date" } },
          count: { $sum: 1 },
        },
      },
    ]),
  ]);

  const createdMap = new Map(createdDeltas.map((d) => [d._id, d.count]));
  const resolvedMap = new Map(resolvedDeltas.map((d) => [d._id, d.count]));
  const closedMap = new Map(closedDeltas.map((d) => [d._id, d.count]));

  const today = formatDate(startOfToday());

  const result = new Map<string, number>();
  let running = createdBefore - resolvedBefore - closedBefore;

  for (const day of days) {
    if (day === today) {
      // FIX: today is never trusted to the per-day aggregation above —
      // computed directly from live current-state counts so a reload
      // immediately after resolving a ticket reflects instantly, with
      // no dependency on resolved_date/closed_date bucket timing.
      const [liveOpen] = await Promise.all([
        Ticket.countDocuments({ tkt_status: { $in: OPEN_STATUSES } }),
      ]);
      result.set(day, liveOpen);
      continue;
    }

    running +=
      (createdMap.get(day) ?? 0) -
      (resolvedMap.get(day) ?? 0) -
      (closedMap.get(day) ?? 0);

    result.set(day, Math.max(0, running));
  }

  return result;
}

