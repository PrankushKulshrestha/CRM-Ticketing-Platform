export interface TrendPoint {
  _id: string;
  count: number;

  /*
   * FIX: added three new series to the same per-day point so the frontend
   * can plot all four lines off a single `trends` array, keyed by date.
   *
   * - resolvedCount: tickets whose resolved_date falls on this day
   *   (event-based, same shape as `count`, just keyed off a different
   *   immutable-once-set date field — accurate going forward).
   * - closedCount: same idea, keyed off closed_date.
   * - openCount: NOT an event count. This is a running balance —
   *   "how many tickets were open as of the end of this day" —
   *   computed as (created on/before day) - (resolved on/before day)
   *   - (closed on/before day). See computeOpenTrend() below.
   */
  resolvedCount: number;
  closedCount: number;
  openCount: number;
}

